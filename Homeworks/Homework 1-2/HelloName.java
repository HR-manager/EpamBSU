public class HelloName {
    /**
     * This method outputs greetings to the name from command line.
     *
     * @param args Text (name) from command line
     * @author Petryakina
     * @version 0.5
     */
    public static void main (String[] args) {
        for (int i = 0; i < 1; i++) {
            System.out.println ("Hello, " + args[i]);
        }
    }
}
